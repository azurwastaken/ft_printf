#include "libft.h"

char	*ft_strstr(const char *str, const char *to_find)
{
	int i;
	int j;

	i = 0;
	if (!*to_find)
		return ((char *)str);
	if (ft_strlen(to_find) > ft_strlen(str))
		return (0);
	while (str[i] != '\0')
	{
		if (str[i] == to_find[0])
		{
			j = 0;
			while (str[i + j] == to_find[j] && to_find[j] != '\0')
				j++;
			if (to_find[j] == '\0')
				return ((char *)(str + i));
			i++;
		}
		else
			i++;
	}
	return (0);
}
