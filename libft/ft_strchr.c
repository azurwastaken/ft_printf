#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (*s != c && *s != '\0')
		s++;
	if (*s == '\0' && c != '\0')
		return (0);
	return ((char *)s);
}
