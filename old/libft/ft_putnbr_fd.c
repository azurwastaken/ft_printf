#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;

	if (n < 0)
	{
		nbr = (long)n * -1;
		ft_putchar_fd('-', fd);
	}
	else
		nbr = (long)n;
	if (nbr <= 9)
	{
		ft_putchar_fd(nbr + '0', fd);
	}
	else
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
}
